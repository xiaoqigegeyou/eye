package cn.pojo;

public class StepPO {

	private Integer id;
	private String step_name;
	private String suffix_name;
	private String step_image;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getStep_name() {
		return step_name;
	}

	public void setStep_name(String step_name) {
		this.step_name = step_name;
	}

	public String getSuffix_name() {
		return suffix_name;
	}

	public void setSuffix_name(String suffix_name) {
		this.suffix_name = suffix_name;
	}

	public String getStep_image() {
		return step_image;
	}

	public void setStep_image(String step_image) {
		this.step_image = step_image;
	}

}